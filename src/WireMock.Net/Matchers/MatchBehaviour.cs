namespace WireMock.Matchers
{
    /// <summary>
    /// MatchBehaviour
    /// </summary>
    public enum MatchBehaviour
    {
        /// <summary>
        /// Accept on match (default)
        /// </summary>
        AcceptOnMatch,

        /// <summary>
        /// Reject on match
        /// </summary>
        RejectOnMatch
    }
}